.equ x_dac, 0xFE10
    .equ y_dac, 0xFE14
    .org 0000h
    ljmp main

    .org 000Bh          ; address for timer 0 overflow interrupt
    ljmp tie_loop

main:
    setb P1.0           ; turn on the laser

    ; X axis starts at 0 
    mov R2, #00h        ; R2 = X Low byte (fraction)
    mov R3, #00h        ; R3 = X High byte (integer phase)

    ; Y axis starts at 64 (40h) for the cosine shift
    mov R4, #00h        ; R4 = Y Low byte (fraction)
    mov R5, #40h        ; R5 = Y High byte (integer phase)

    ; configure TMOD for timer 0, Mode 2 (8-bit auto-reload)
    mov TMOD, #02h

    ; enable interrupts 
    mov IE, #82h

    ; load auto-reload value for 5120 Hz
    mov TH0, #04Ch      
    mov TL0, #04Ch      

    ; start timer
    setb TR0

main_loop:
    sjmp main_loop      ; Idle here forever while interrupts do the work

tie_loop:
    ; x advance
    mov A, R2           
    add A, #00h         ; Add fractional step (0.25 = 40h)
    mov R2, A           
    
    mov A, R3           
    addc A, #01h        ; Add integer step (1) + carry
    mov R3, A           

    ; output for x
    mov DPTR, #sine_table
    movc A, @A+DPTR     
    mov DPTR, #x_dac
    movx @DPTR, A       

    ; y advance
    mov A, R4           
    add A, #00h        ; Add fractional step (0.75 = C0h)
    mov R4, A           
    
    mov A, R5           
    addc A, #03h        ; Add integer step (3) + carry
    mov R5, A           

    ; output for y
    mov DPTR, #sine_table
    movc A, @A+DPTR     
    mov DPTR, #y_dac 
    movx @DPTR, A       

    reti

